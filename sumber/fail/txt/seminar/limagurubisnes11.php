Nombor 3:

Menerapkan islam dalam dunia perniagaan sangat mencabar.

Kena pula ada banyak mazhab dalam islam, apa yang kita kata islam akan ada orang lain menentang pendapat kita yang mengatakan bahawa itu bukan islam.

Kadang-kala kita harus melihat sirah nabi dari sudut berbeza.

Dalam definasi lama, nabi muhammad dianggap mengambil upah mengembala kambing.

Dalam definasi baru, nabi muhammad dianggap buat servis mengembala kambing.

Itu serupa dengan kes ambil upah potong rumput atau buat bisnes potong rumput.

Begitu juga dengan kes teksi sapu atau buat grabcar.

Aktiviti yang sama, definasi yang berbeza.

