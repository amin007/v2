Bismillah. Seminar di malaysia - bahagian 11.

Ulasan kita tentang seminar 5 guru bisnes.

Orang kita sekarang sudah berani tanya soalan, cuma tidak pandai tanya soalan pendek.

Misalannya soalan pendek adalah tiga tolak satu sama dengan berapa.

Orang kita tanya soalan panjang seperti boboiboy pergi kedai koko tok abah ikut jalan jauh.

Naik bukit, turun bukit, dikejar anjing.

Pada hal kedai koko tok abah depan rumah sahaja.

Bagi kita itu satu perkembangan yang menarik.

Antara soalan yang ditanyakan adalah macam mana hendak cari pekerja aka staf yang muda dan pujuk agar setia dan dengar cakap.

Beberapa guru bisnes cakap, method kita terhadap orang muda kena berubah.

Definasi kita bekerja keras berbeza dengan definasi orang muda bekerja keras.

Mereka(guru bisnes) pun tidak tahu sejauh mana orang muda boleh dipercayai jika mereka tidak diberi peluang.

Mereka cakap harus bagi peluang dan kepercayaan kepada orang muda sehingga mereka kianati kepercayaan kita.

Ada yang takut tidak ada bisnes lagi di masa hadapan.

Apabila mendengar beberapa guru bisnes bercerita tentang pengalaman mereka dalam bisnes, kebanyakan orang kita merasa tenang dan yakin meneruskan cabaran dalam bisnes di masa hadapan.

